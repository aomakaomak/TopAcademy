a = int(input("Введите четырехзначное число"))
# сделал только с оператором //,  без оператора %

a1 = a // 1000
print(a1)

a2 = (a - a1 * 1000) // 100
print(a2)

a3 = (a - (a // 100 * 100)) // 10
print(a3)

a4 = a - a // 10 * 10
print(a4)

if a1 + a4 == a2 + a3:
    print("Сумма равна")
else:
    print("Сумма не равна")
